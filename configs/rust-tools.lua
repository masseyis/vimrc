  },
  server = {
    on_attach = function(_, bufnr)
      -- Hover actions
      vim.keymap.set("n", "<C-space>", rt.hover_actions.hover_actions, { buffer = bufnr })
      -- Code action groups
      vim.keymap.set("n", "<Leader>a", rt.code_action_group.code_action_group, { buffer = bufnr })
    end,
    capabilities = capabilities,
  },
}
local lldb = {
	name = "Attach to Rust",
	type = "rt_lldb", -- matches the adapter
	request = "attach", -- could also attach to a currently running process
  pid = "${command:pickProcess}",
}
local launch_lldb = {
	name = "Launch lldb",
	type = "rt_lldb", -- matches the adapter
	request = "launch", -- could also attach to a currently running process
	program = function()
		return vim.fn.input(
			"Path to executable: ",
			vim.fn.getcwd() .. "/",
			"file"
		)
	end,
	cwd = "${workspaceFolder}",
	stopOnEntry = false,
	args = {},
	runInTerminal = false,
}
local launched_lldb
require('dap').configurations.rust = {
	lldb,
  launch_lldb,
}

